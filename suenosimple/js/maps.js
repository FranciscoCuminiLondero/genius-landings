  /* ── Verificar CP ─────────────────────────────────────── */
  function checkCP() {
    const raw    = document.getElementById('cp-input').value.trim();
    const result = document.getElementById('cp-result');
    const cp     = parseInt(raw, 10);
  
    result.className = 'cp-result';
    result.classList.remove('hidden');
  
    if (!raw || isNaN(cp)) {
      result.classList.add('none');
      result.innerHTML = '⚠️ Ingresá un código postal válido (4 dígitos).';
      return;
    }
  
    const zone = CP_ZONES[cp];
    if (zone === 1) {
      result.classList.add('ok');
      result.innerHTML = '🟢 ¡Llegamos! Entrega en <strong>24 hs hábiles</strong> — zona CABA.';
      highlightZone(1);
    } else if (zone === 2) {
      result.classList.add('warn');
      result.innerHTML = '🟡 ¡Llegamos! Entrega en <strong>48 hs hábiles</strong> — GBA 1° cordón.';
      highlightZone(2);
    } else if (zone === 3) {
      result.classList.add('slow');
      result.innerHTML = '🔴 ¡Llegamos! Entrega en <strong>72 hs hábiles</strong> — GBA 2° cordón.';
      highlightZone(3);
    } else {
      result.classList.add('none');
      result.innerHTML = '😔 Ese código postal está fuera de nuestra zona de cobertura actual.';
    }
  }
  
  document.addEventListener('DOMContentLoaded', function () {
    document.getElementById('cp-input')
      .addEventListener('keydown', e => e.key === 'Enter' && checkCP());
  });
  
